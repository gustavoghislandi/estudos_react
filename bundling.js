// Bundle é o todo da build "empacotado".

// Em React, bundling é o processo de juntar vários arquivos do projeto (componentes, JS, CSS, imagens) em um ou poucos arquivos finais para o navegador carregar mais rápido.

// Ferramentas como Webpack, Vite ou Parcel fazem isso automaticamente.

// Exemplo simples:

// Você escreve:

    // App.jsx
    // Button.jsx
    // style.css

// Depois do build, tudo vira algo como:

    // bundle.js

// Esse bundle.js contém todo o código necessário para a aplicação funcionar no browser.

// Em React, o bundle é o pacote final da build.
// É todo o código da aplicação empacotado (JS, dependências, às vezes CSS) em um ou mais arquivos prontos para o navegador.

