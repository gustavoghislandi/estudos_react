// RSC significa “React Server Components”.

// É um recurso do React que permite renderizar componentes no servidor, enviando apenas HTML + dados para o cliente, sem incluir o código JS desses componentes no bundle final.

// Em poucas palavras:

    // RSC = componentes React que rodam no servidor
    // Cliente recebe menos JS
    // Melhora performance e TTI

// Ideal para apps grandes com muita renderização de conteúdo